package com.freakypulse.writenote;

public class NavView {
	
	int icon_id;
	String icon_text;
	public NavView(int icon_id, String icon_text) {
		super();
		this.icon_id = icon_id;
		this.icon_text = icon_text;
	}
	public int getIcon_id() {
		return icon_id;
	}
	public String getIcon_text() {
		return icon_text;
	}
	

}
